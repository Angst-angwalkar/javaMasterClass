package HelloWorld;

public class Basics {

    public static void main(String[] args) {
        System.out.println("Hello Bitches in the World!");

        int myFirstNumber = (19 + 2) + (3 * 21);
        System.out.println(myFirstNumber);

        int mySecondNumber = 12;
        System.out.println(mySecondNumber);

        int myThirdNumber = 6;
        System.out.println(myThirdNumber);

        int myLastNumber = 1000;
        System.out.println(myLastNumber);

        int myTotal = myFirstNumber + (mySecondNumber + myThirdNumber) - myLastNumber;
        System.out.println(myTotal);
    }
}
